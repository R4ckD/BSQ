#include "bsq_all-h"

int	ft_atoi_simply(char *str)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		res = res * 10 + (int)str[i] - '0';
		i++;
	}
	return (res);
}
